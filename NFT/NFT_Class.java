package NFT;

public class NFT_Class {
    private int id;
    private String name;
    private String path;
    private int weight;

    public NFT_Class(int id, String name, String path, int weight) {
        this.id = id;
        this.name = name;
        this.path = path;
        this.weight = weight;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getPath() {
        return path;
    }

    public void setPath(String path) {
        this.path = path;
    }

    public int getWeight() {
        return weight;
    }

    public void setWeight(int weight) {
        this.weight = weight;
    }
}
